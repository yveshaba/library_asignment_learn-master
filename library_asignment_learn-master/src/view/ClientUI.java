    public void view(){
        List<Client> list = new ClientDao().findAll();
        String[] title = {"#","Names","Client Category", "Gender"};
        Object[][] data = new Object[list.size()][title.length];
        int i = 0;
        
        for(Client b: list){
            data[i][0] = b.getClientId();
            data[i][1] = b.getNames();
            data[i][2] = b.getClientCategory();
            data[i][3] = b.getGender();
            i++;
        }
        
        jTable1.setModel(new DefaultTableModel(data, title));
    }

