    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            ClientDao cd = new ClientDao();
            Client c = new Client();
            
            c.setClientId(TFcid.getText());
            c.setNames(TFnames.getText());
            c.setGender(jComboBoxgender.getSelectedItem().toString());
            
            if(jComboBoxcategory.getSelectedItem().toString().equalsIgnoreCase("Staff")){
                c.setClientCategory(ClientCategory.STAFF);
            }else if(jComboBoxcategory.getSelectedItem().toString().equalsIgnoreCase("Student")){
                c.setClientCategory(ClientCategory.STUDENT);
            }else if(jComboBoxcategory.getSelectedItem().toString().equalsIgnoreCase("External")){
                c.setClientCategory(ClientCategory.EXTERNAL);
            }
            
//            new ClientDao().register(c);
            cd.register(c);
            view();
            
            JOptionPane.showMessageDialog(rootPane, "Client Registered");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

